function loadJson(filePath) {
  try {
    const content = fs.readFileSync(filePath, 'utf8');
    return JSON.parse(content);
  } catch (error) {
    console.error(`Error loading ${filePath}:`, error.message);
    return null;
  }
}

function saveJson(filePath, data) {
  const content = JSON.stringify(data, null, 2);
  fs.writeFileSync(filePath, content, 'utf8');
}

function getLanguageCode(filename) {
  return filename.replace('.json', '');
}

function translateValue(englishValue, langCode) {
  const langTranslations = TRANSLATIONS[langCode];
  if (!langTranslations) {
    return englishValue;
  }

  // Check for direct match
  if (langTranslations[englishValue]) {
    return langTranslations[englishValue];
  }

  // For untranslated values, return English as fallback
  return englishValue;
}

function syncLocale(referencePath, targetPath) {
  const reference = loadJson(referencePath);
  const target = loadJson(targetPath);

  if (!reference || !target) {
    return { added: 0, removed: 0 };
  }

  const langCode = getLanguageCode(path.basename(targetPath));
  const referenceKeys = Object.keys(reference);
  const targetKeys = new Set(Object.keys(target));

  let added = 0;
  let removed = 0;

  // Create new object with all reference keys in order
  const synced = {};

  for (const key of referenceKeys) {
    if (targetKeys.has(key)) {
      // Keep existing translation
      synced[key] = target[key];
    } else {
      // Add missing key with translation
      synced[key] = translateValue(reference[key], langCode);
      added++;
    }
  }

  // Count removed keys
  for (const key of targetKeys) {
    if (!referenceKeys.includes(key)) {
      removed++;
    }
  }

  // Save synced file
  saveJson(targetPath, synced);

  return { added, removed };
}

function main() {
  console.log('Syncing locale files...\n');

  const referencePath = path.join(LOCALES_DIR, REFERENCE_LOCALE);
  const reference = loadJson(referencePath);

  if (!reference) {
    console.error('Could not load reference locale file');
    process.exit(1);
  }

  const referenceKeyCount = Object.keys(reference).length;
  console.log(`Reference (${REFERENCE_LOCALE}): ${referenceKeyCount} keys\n`);

  const files = fs.readdirSync(LOCALES_DIR)
    .filter(f => f.endsWith('.json') && f !== REFERENCE_LOCALE);

  for (const file of files) {
    const targetPath = path.join(LOCALES_DIR, file);
    const beforeKeys = Object.keys(loadJson(targetPath) || {}).length;

    const { added, removed } = syncLocale(referencePath, targetPath);

    const afterKeys = Object.keys(loadJson(targetPath) || {}).length;

    console.log(`${file}:`);
    console.log(`  Before: ${beforeKeys} keys`);
    console.log(`  After: ${afterKeys} keys`);
    console.log(`  Added: ${added}, Removed: ${removed}`);
    console.log();
  }

  console.log('Sync complete!');
}

main();
